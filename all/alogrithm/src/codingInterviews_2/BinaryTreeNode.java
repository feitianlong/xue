package codingInterviews_2;

public class BinaryTreeNode {
	int val;
	BinaryTreeNode left_n;
	BinaryTreeNode right_n;

	public BinaryTreeNode() {
		this.val = 0;
		this.left_n = null;
		this.right_n = null;
	}

	public BinaryTreeNode(int val) {
		this.val = val;
		this.left_n = null;
		this.right_n = null;
	}
}
